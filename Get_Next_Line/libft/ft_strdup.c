#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	int		lenght;
	char	*new_str;

	lenght = 0;
	while (str[lenght])
		lenght++;
	if (!(new_str = (char*)malloc(sizeof(char) * (lenght + 1))))
		return (NULL);
	i = 0;
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
