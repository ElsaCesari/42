#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t x;

	x = 0;
	if (s && f)
	{
		while (s[x] != '\0')
		{
			(*f)(&s[x]);
			x++;
		}
	}
}
