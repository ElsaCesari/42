#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int x;

	i = 0;
	while (s1[i])
		i++;
	x = 0;
	while (s2[x])
	{
		s1[i] = s2[x];
		x++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
