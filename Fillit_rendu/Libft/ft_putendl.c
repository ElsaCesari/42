#include "libft.h"

void	ft_putendl(char const *s)
{
	int x;

	x = 0;
	if (!s)
		return ;
	while (((char *)s)[x])
	{
		write(1, &((char *)s)[x], 1);
		x++;
	}
	write(1, "\n", 1);
}
